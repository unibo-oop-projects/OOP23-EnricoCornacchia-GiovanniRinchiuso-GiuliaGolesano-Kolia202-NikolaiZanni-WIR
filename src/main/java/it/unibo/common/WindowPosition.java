package it.unibo.common;
/**
 * Class to manage a window position.
 */
public class WindowPosition {
    private int x;
    private int y;

    public WindowPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}

