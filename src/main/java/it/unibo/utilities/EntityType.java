package it.unibo.utilities;

/**
 * Represents the types of entities in the game.
 * Each entity type corresponds to a specific category of game objects.
 */
public enum EntityType {
    /**
     * Represents the player character.
     */
    FELIX,
    /**
     * Represents the enemy character.
     */
    RALPH,
    /**
     * Represents the power-up entity.
     */
    POWER_UP,
    /**
     * Represents the obstacle entity.
     */
    BRICK,
    /**
     * Represents the interactable entity.
     */
    WINDOW,
    
}